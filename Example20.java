package Stream;

//Method reference in stream

public class Example20 {

    int id;
    String name;
    float price;

    public Example20(int id, String name, float price){
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public int getId(){
        return id;
    }

    public String getName(){
        return name;
    }

    public float getPrice(){
        return price;
    }
}
